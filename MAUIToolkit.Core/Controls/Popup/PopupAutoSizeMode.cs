namespace MAUIToolkit.Core.Controls.Popup;

//
// Summary:
//     Defines constant that specify how the PopupView is Sized.
public enum PopupAutoSizeMode
{
    //
    // Summary:
    //     PopupView's width will be automatically adjusted based on the contents of its
    //     template.
    Width,
    //
    // Summary:
    //     PopupView's height will be automatically adjusted based on the contents of its
    //     template.
    Height,
    //
    // Summary:
    //     PopupView's width and height will be automatically adjusted based on the contents
    //     of its template.
    Both,
    //
    // Summary:
    //     PopupView's size will not be determined by the contents of its template. This
    //     is the default value.
    None
}

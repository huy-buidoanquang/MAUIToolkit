namespace MAUIToolkit.Core.Controls.Popup;

//
// Summary:
//     Defines constants that specifies whether the overlay should be transparent or
//     blurred.
public enum PopupOverlayMode
{
    //
    // Summary:
    //     Specifies that the overlay will be transparent with opacity applied based on
    //     value of MAUIToolkit.Core.Controls.Popup.PopupStyle.OverlayColor alpha.
    Transparent,
    //
    // Summary:
    //     Specifies that the overlay will be blurred based on the intensity of MAUIToolkit.Core.Controls.Popup.PopupStyle.BlurIntensity.
    Blur
}
